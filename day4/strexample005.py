# Example 005: String Concatenation
money= 568.8912
name= "John"
print('name is {0}- age is {1}- money is {2}'.format(name,20,money))
print('name is {0:s}- age is {1:d}- money is {2:f}'.format(name,20,money))
print('name is {0:s}- age is {1:d}- money is {2:0.2f}'.format(name,20,money))
