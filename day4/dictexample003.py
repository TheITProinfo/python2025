# this is a example of dictionary in python

# create a dictionary
dict1={"name":"John","age":30,"city":"New York"}

# print the dictionary
print(dict1)
print(type(dict1))
print(dict1.items()) # print all key-value pairs in the dictionary
print(dict1.keys()) # print all keys in the dictionary
print(dict1.values()) # print all values in the dictionary
print(list(dict1.values())) # print all values in the dictionary as a list

